package day01_helloworld;

public class D01_Variable {
    public static void main(String[] args) {
        int myInt = 10;
        System.out.println(myInt);
        myInt = 20;
        System.out.println(myInt);
        myInt = myInt + 5;
        System.out.println(myInt);
        System.out.println(myInt + 5);
    }
}
